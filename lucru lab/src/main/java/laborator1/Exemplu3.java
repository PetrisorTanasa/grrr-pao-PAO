package main.java.laborator1;

public class Exemplu3 {
    public static void main(String[] args) {
        System.out.println(5 % 4);
        System.out.println(5 / 4);
        System.out.println(5.0 / 4);
        System.out.println(5 / 4.0);
        System.out.println(5.0 / 4.0);
        System.out.println(5.0 / 4.0 * 2);
        System.out.println(5.0 / (4.0 * 2));
        System.out.println(5.0 / 4.0 * 2 + 1);
        System.out.println(5.0 / (4.0 * 2) + 1);
        System.out.println(5.0 / (4.0 * 2) + 1 - 2);
        System.out.println(5.0 / (4.0 * 2) + 1 - 2 * 3);
    }
}
